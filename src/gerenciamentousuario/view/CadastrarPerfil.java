    /**
     * Método que realiza o cadastro no sistema após o botão cadastrar ser apertado.
     * @param evt - ActionEvent - evento de click recebido pelo sistema.
     */
    private void buttonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCadastrarActionPerformed
        String entrada = textNome.getText();
        if (entrada.length() < 1) {
            JOptionPane.showMessageDialog(this, "Erro no cadastro", "Cadastro de Perfil", JOptionPane.ERROR_MESSAGE);
        } else {
            new PerfilController().adiciona(new Perfil(entrada));
            JOptionPane.showMessageDialog(this, "Cadastrado com sucesso", "Cadastro de Perfil", JOptionPane.INFORMATION_MESSAGE);
            try {
                origem.atualizarTabela();
            } catch (ParseException ex) {
                java.util.logging.Logger.getLogger(CadastrarPerfil.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
            }
            
            this.dispose();
        }
    }//GEN-LAST:event_buttonCadastrarActionPerformed
